
class B04_SimpleInterest {

    public static void main(String[] args) {
        // simple interest = principle * rate * time 
        double p = 3000;
        double r = 1.5, t = 5;
        double simpleInterest = (p * r * t) / 100;
        System.out.println(" Simpel interest is : " + simpleInterest);

    }
}
